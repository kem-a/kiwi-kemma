def update_language(po_path: Path, translations: dict[str, str]) -> bool:
    po = polib.pofile(po_path)
    changed = False
    for entry in po:
        if entry.obsolete:
            continue
        translated_value = translations.get(entry.msgid)
        if translated_value:
            if entry.msgstr != translated_value:
                entry.msgstr = translated_value
                if 'fuzzy' in entry.flags:
                    entry.flags.remove('fuzzy')
                changed = True
    if changed:
        po.save()
    return changed


def main() -> None:
    po_dir = Path(__file__).resolve().parents[1] / 'po'
    for po_path in sorted(po_dir.glob('*.po')):
        lang = po_path.stem
        translations = LANG_TRANSLATIONS.get(lang)
        if not translations:
            continue
        updated = update_language(po_path, translations)
        status = 'updated' if updated else 'up-to-date'
        print(f"{po_path.name}: {status}")


if __name__ == '__main__':
    main()
